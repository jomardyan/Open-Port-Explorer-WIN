    public string GetKnownProcessDescription(string processName)
    {
        if (string.IsNullOrWhiteSpace(processName) || string.Equals(processName, "N/A", StringComparison.OrdinalIgnoreCase))
        {
            return "Process information not available.";
        }

        var normalizedName = processName.EndsWith(".exe", StringComparison.OrdinalIgnoreCase)
            ? processName[..^4]
            : processName;

        return KnownProcessDescriptions.TryGetValue(normalizedName, out var description)
            ? description
            : "No known description for this process.";
    }
